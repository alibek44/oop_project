package Classes;

public class Customer {
    private String cName;
    private int cPhone;

    public String getCName(){
        return cName;
    }
    public void setCName(String name){
        this.cName = name;
    }
    public int getCPhone(){
        return cPhone;
    }
    public void setCPhone(int phone){
        this.cPhone = phone;
    }
}
